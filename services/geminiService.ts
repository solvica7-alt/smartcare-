// Internal helper to call API via REST
async function callGeminiAPI(payload: any, customModel?: string) {

    // Convert Gemini payload to OpenAI format
    const messages = [];
    
    if (payload.systemInstruction) {
        let sysText = "";
        if (payload.systemInstruction.parts) {
            sysText = payload.systemInstruction.parts.map((p:any) => p.text).join('\n');
        }
        if (sysText) {
            messages.push({ role: 'system', content: sysText });
        }
    }

    if (payload.contents) {
        for (const content of payload.contents) {
            const role = content.role === 'model' ? 'assistant' : 'user';
            const parts = content.parts || [];
            
            const oaiContent = [];
            for (const part of parts) {
                if (part.text) {
                    oaiContent.push({ type: "text", text: part.text });
                } else if (part.inlineData) {
                    oaiContent.push({
                        type: "image_url",
                        image_url: {
                            url: `data:${part.inlineData.mimeType};base64,${part.inlineData.data}`
                        }
                    });
                }
            }
            
            if (oaiContent.length === 1 && oaiContent[0].type === "text") {
                messages.push({ role, content: oaiContent[0].text });
            } else {
                messages.push({ role, content: oaiContent });
            }
        }
    }
    
    // Default to Mistral Large for smarter context understanding
    const modelToUse = customModel === 'gpt-4o' ? MODEL_NAME : (customModel || MODEL_NAME);

    const openaiPayload = {
        model: modelToUse, 
        messages: messages,
        max_tokens: 1024,
        temperature: 0.3
    };

    try {
        const response = await fetch(API_URL, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${GEMINI_API_KEY}`,
            },
            body: JSON.stringify(openaiPayload)
        });

        if (!response.ok) {
            const errorText = await response.text();
            console.error(`Mistral API Error (${response.status}):`, errorText);
            throw new Error(`API Error ${response.status}: ${errorText}`);
        }

        const data = await response.json();
        
        if (!data.choices || data.choices.length === 0) {
            throw new Error("No response generated by Mistral.");
        }

        return data.choices[0].message.content;

    } catch (error) {
        console.error("Mistral API call failed:", error);
        throw error;
    }
}

